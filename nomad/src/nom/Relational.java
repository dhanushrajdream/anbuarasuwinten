package nom;

public class Relational {
	public static void main(String[] args) {
		long no1=12457965l;
		long no2=12457965l;
		System.out.println(no1<no2);
		System.out.println(no1>no2);
		System.out.println(no1<=no2);
		System.out.println(no1>=no2);
		System.out.println(no1==no2);
		System.out.println(no1!=no2);
	}

}
