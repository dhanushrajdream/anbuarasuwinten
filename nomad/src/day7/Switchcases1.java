package day7;

public class Switchcases1 {
	public static void main(String[] args) {
		String month="J";
		switch(month) {
		case "Ja":System.out.println("January");
		break;
		case "F":System.out.println("February");
		break;
		case "Mar":System.out.println("March");
		break;
		case "A":System.out.println("April");
		break;
		case "M":System.out.println("May");
		break;
		case "J":System.out.println("June");
		break;
		case "Ju":System.out.println("July");
		break;
		case "Au":System.out.println("August");
		break;
		case "S":System.out.println("September");
		break;
		case "O":System.out.println("October");
		break;
		case "N":System.out.println("November");
		break;
		case "D":System.out.println("December");
		break;
		default :System.out.println("Invalid data");
		}
	}

}
